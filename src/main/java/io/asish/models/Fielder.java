package io.asish.models;

public class Fielder {
	
	int catches;
	int stumping;
	int directRunout;
	int InDirectRunOut;
	public int getCatches() {
		return catches;
	}
	public void setCatches(int catches) {
		this.catches = catches;
	}
	public int getStumping() {
		return stumping;
	}
	public void setStumping(int stumping) {
		this.stumping = stumping;
	}
	public int getDirectRunout() {
		return directRunout;
	}
	public void setDirectRunout(int directRunout) {
		this.directRunout = directRunout;
	}
	public int getInDirectRunOut() {
		return InDirectRunOut;
	}
	public void setInDirectRunOut(int inDirectRunOut) {
		InDirectRunOut = inDirectRunOut;
	}

}
